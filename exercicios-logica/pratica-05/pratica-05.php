5 - Você foi contratado para desenvolver um gerador de senhas em PHP, onde o usuário poderá configurar o tamanho da senha e quais caracteres deseja adicionar.

O programa deve seguir as restrições abaixo: 
- O programa deve permitir que o usuário informe o tamanho desejado para a senha, mas deve ter um limite entre 6 e 60 caracteres.
- O programa deve permitir que o usuário defina se deseja ter letras maiúsculas, letras minúsculas, caracteres numéricos e caracteres especiais.
- O programa deve impedir a geração da senha caso nenhum tipo de caractere for definido.