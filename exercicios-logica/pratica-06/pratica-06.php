6 - Você foi contratado para desenvolver uma calculadora para linha de comando, onde o usuário deverá digitar a expressão matemática simples que deseja calcular e o programa deverá executá-la e retornar o resultado para o usuário.

O programa deve seguir as restrições abaixo: 
- O programa deve permitir que o usuário insira uma expressão matemática simples.
- O programa deve verificar o conteúdo da expressão e permitir que apenas caracteres numéricos, operadores matemáticos, espaços e parênteses possam ser enviados.
- O programa deve imprimir uma mensagem de erro caso a expressão não seja válida. 
- 0 programa deve executar a expressão matemática e imprimir o resultado para o usuário. 
- 0 programa deve imprimir uma mensagem de erro caso o resultado não seja numérico.